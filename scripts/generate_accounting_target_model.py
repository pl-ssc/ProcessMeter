    l2_count = len(STRUCTURE)
    l3_count = sum(len(l2["level_3"]) for l2 in STRUCTURE)
    l4_count = sum(len(l3["level_4"]) for l2 in STRUCTURE for l3 in l2["level_3"])
    executor_distribution = {"business": 0, "front": 0, "middle": 0}
    for l2 in STRUCTURE:
        for l3 in l2["level_3"]:
            for _, executor in l3["level_4"]:
                executor_distribution[executor] += 1
    return {
        "process_1_id": PROCESS_1_ID,
        "process_1_name": PROCESS_1_NAME,
        "level_2_count": l2_count,
        "level_3_count": l3_count,
        "level_4_count": l4_count,
        "executor_distribution": executor_distribution,
    }


